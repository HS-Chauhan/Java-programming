import java.util.*;
  
class trignometry{
  
    // Driver Code
    public static void main(String args[])
    {
  
        double degrees = 45.0;
  
        
        double radians = Math.toRadians(degrees);
  
        
        double sinValue = Math.sin(radians);
  
        
        System.out.println("sin(" + degrees + ") = " + sinValue);
    }
}
