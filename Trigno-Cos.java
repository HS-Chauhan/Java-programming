import java.util.*;
  
class GFG {
  
   
    public static void main(String args[])
    {
  
        double degrees = 45.0;
  
        
        double radians = Math.toRadians(degrees);
  
       
        double cosValue = Math.cos(radians);
  
        
        System.out.println("cos(" + degrees + ") = " + cosValue);
    }
}
